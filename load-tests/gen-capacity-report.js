#!/usr/bin/env node
const fs   = require('fs');
const path = require('path');

const capDir = process.argv[2] || 'results/capacity';

const BUDGET = {
  simple: Number(process.env.BUDGET_SIMPLE || 50),
  proxy:  Number(process.env.BUDGET_PROXY  || 100),
  crud:   Number(process.env.BUDGET_CRUD   || 150),
};

const WL_LABEL = {
  simple: 'Hot path `/webhook/:slug` (no proxy, no logging)',
  proxy:  'Hot path `/webhook/:slug` (template + proxy)',
  crud:   'CRUD `/api/webhooks` (create/get/toggle/delete)',
};

const WL_ORDER = ['simple', 'proxy', 'crud'];

const files = fs.existsSync(capDir)
  ? fs.readdirSync(capDir).filter((f) => f.startsWith('cap_') && f.endsWith('.json'))
  : [];

const runs = files
  .map((f) => {
    try { return JSON.parse(fs.readFileSync(path.join(capDir, f), 'utf8')); }
    catch { return null; }
  })
  .filter(Boolean);

const caches   = [...new Set(runs.map((r) => r.cache))].sort();
const verts    = [...new Set(runs.map((r) => r.verticles))].sort((a, b) => a - b);
const workloads = [...new Set(runs.map((r) => r.workload))];
const wlSorted  = WL_ORDER.filter((w) => workloads.includes(w));

function sel(phase, cache, v, wl) {
  return runs.filter(
    (r) => r.phase === phase && r.cache === cache && r.verticles === v && r.workload === wl,
  );
}

function pass(r, wl) {
  return r.achieved_ratio >= 0.99
      && r.error_rate <= 0.1
      && r.p99 > 0
      && r.p99 <= BUDGET[wl]
      && r.dropped_iterations === 0;
}

function maxRps(cache, v, wl) {
  return sel('capacity', cache, v, wl)
    .filter((r) => pass(r, wl))
    .reduce((m, r) => Math.max(m, r.target_rps), 0);
}

let out = '';

out += `<!-- generated by gen-capacity-report.js at ${new Date().toISOString()} -->\n\n`;
out += '# Capacity & performance characterization\n\n';
out += `Open-model (constant arrival rate) staircase sweep across ` +
  `**cache {${caches.join(', ')}} × verticles {${verts.join(', ')}}**. ` +
  `A step **passes the SLO** when achieved ≥ 99 % of target, error ≤ 0.1 %, no dropped iterations, ` +
  `and p99 ≤ budget (simple ${BUDGET.simple} ms, proxy ${BUDGET.proxy} ms, crud ${BUDGET.crud} ms). ` +
  `The highest passing step = **max sustainable RPS @ SLO**.\n\n`;

// capacity matrix
out += '## Capacity matrix — max sustainable RPS @ SLO\n\n';
for (const wl of wlSorted) {
  out += `### ${WL_LABEL[wl]} — budget p99 ≤ ${BUDGET[wl]} ms\n\n`;
  out += `| verticles \\ cache | ${caches.map((c) => `\`${c}\``).join(' | ')} |\n`;
  out += `|---|${caches.map(() => '---:').join('|')}|\n`;

  for (const v of verts) {
    const cells = caches.map((c) => {
      const r = maxRps(c, v, wl);
      return r > 0 ? `**${r}**` : '—';
    });
    out += `| ${v} | ${cells.join(' | ')} |\n`;
  }
  out += '\n';
}

// staircase detail
out += '## Staircase detail (per config)\n\n';
for (const cache of caches) {
  for (const v of verts) {
    for (const wl of wlSorted) {
      const rows = sel('capacity', cache, v, wl).sort((a, b) => a.target_rps - b.target_rps);
      if (!rows.length) continue;

      out += `### cache=\`${cache}\` · verticles=${v} · ${wl}\n\n`;
      out += '| target rps | achieved | p50 | p95 | p99 | err % | dropped | vus | SLO |\n';
      out += '|---:|---:|---:|---:|---:|---:|---:|---:|:---:|\n';

      for (const r of rows) {
        const slo = pass(r, wl) ? '✅' : '❌';
        out += `| ${r.target_rps} | ${r.achieved_rps} | ${r.p50} | ${r.p95} | ${r.p99}` +
               ` | ${r.error_rate} | ${r.dropped_iterations} | ${r.vus_max} | ${slo} |\n`;
      }

      const mx = maxRps(cache, v, wl);
      out += `\n**Max sustainable @ SLO: ${mx > 0 ? mx + ' rps' : 'none'}**\n\n`;
    }
  }
}

// soak
const soaks = runs.filter((r) => r.phase === 'soak');
if (soaks.length) {
  out += '## Soak (stability at ~80 % of max, simple hot path)\n\n';
  out += '| config | rps | p50 | p95 | p99 | max | err % | dropped |\n';
  out += '|---|---:|---:|---:|---:|---:|---:|---:|\n';

  const sorted = soaks.sort((a, b) => (a.cache + a.verticles).localeCompare(b.cache + b.verticles));
  for (const r of sorted) {
    out += `| cache=${r.cache} v${r.verticles}` +
           ` | ${r.achieved_rps} | ${r.p50} | ${r.p95} | ${r.p99}` +
           ` | ${r.max} | ${r.error_rate} | ${r.dropped_iterations} |\n`;
  }
  out += '\nMemory growth start→end is in `results/capacity/stats_*_soak_*.txt`.\n\n';
}

// spike
const spikes = runs.filter((r) => r.phase === 'spike');
if (spikes.length) {
  out += '## Spike (2× burst above sustained, simple hot path)\n\n';
  out += '| config | peak target | achieved | p99 | max | err % | dropped |\n';
  out += '|---|---:|---:|---:|---:|---:|---:|\n';

  const sorted = spikes.sort((a, b) => (a.cache + a.verticles).localeCompare(b.cache + b.verticles));
  for (const r of sorted) {
    out += `| cache=${r.cache} v${r.verticles}` +
           ` | ${r.target_rps || '-'} | ${r.achieved_rps}` +
           ` | ${r.p99} | ${r.max} | ${r.error_rate} | ${r.dropped_iterations} |\n`;
  }
  out += '\n';
}

out += '---\n\n## SLI / SLO / SLA\n\n';
out += '_Analysis authored below from these numbers._\n';

process.stdout.write(out);
